//this function listens to keystrokes and filters the user input with the fruit array in a lower case. 
input.onkeyup = function search() {
	let results = [];
	let userInput = input.value;			
	if(userInput.length){
		results = fruit.filter((keyword) => {
			return keyword.toLowerCase().includes(userInput.toLowerCase());
		});
		console.log(results);
	}
	showSuggestions(results)
}


function showSuggestions(results) {
	const dropDown = results.map((inputVal) => {
	return "<li onclick=useSuggestion(this)>" + inputVal + "</li>"
	})
	suggestions.innerHTML = "<ul>" + dropDown.join('') +"</ul>" //.join removes comma
}

function useSuggestion(e) {
	input.value = e.innerHTML;
	suggestions.innerHTML = '';
}
